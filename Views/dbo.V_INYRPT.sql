CREATE VIEW [dbo].[V_INYRPT] AS			
  SELECT ASTORE, ASETTLENO, BGDGID, BWRH,			
    SUM(CQ1) CQ1, SUM(CQ2) CQ2, SUM(CQ3) CQ3, SUM(CQ4) CQ4,			
    SUM(CT1) CT1, SUM(CT2) CT2, SUM(CT3) CT3, SUM(CT4) CT4,			
    SUM(CI1) CI1, SUM(CI2) CI2, SUM(CI3) CI3, SUM(CI4) CI4,			
    SUM(CR1) CR1, SUM(CR2) CR2, SUM(CR3) CR3, SUM(CR4) CR4,			
    SUM(DQ1) DQ1, SUM(DQ2) DQ2, SUM(DQ3) DQ3, SUM(DQ4) DQ4,			
    SUM(DT1) DT1, SUM(DT2) DT2, SUM(DT3) DT3, SUM(DT4) DT4,			
    SUM(DI1) DI1, SUM(DI2) DI2, SUM(DI3) DI3, SUM(DI4) DI4,			
    SUM(DR1) DR1, SUM(DR2) DR2, SUM(DR3) DR3, SUM(DR4) DR4			
  FROM INYRPT			
  GROUP BY ASTORE, ASETTLENO, BGDGID, BWRH			

GO
