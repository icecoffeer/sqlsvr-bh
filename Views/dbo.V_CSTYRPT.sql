CREATE VIEW	[dbo].[V_CSTYRPT]	(	
		ASTORE,	
		BCSTGID	,
		BGDGID	,
		BWRH	,
		NPQTY	,
		NPTL	
	) AS SELECT		
		ASTORE,	
		BCSTGID,	
		BGDGID,	
		BWRH,	
		(CQ3-CQ1)+(DQ3-DQ1),	
		(CT3-CT1)+(DT3-DT1)	
	FROM CSTYRPT WHERE ASETTLENO = (SELECT MAX(NO) FROM YEARSETTLE)		
	
GO
