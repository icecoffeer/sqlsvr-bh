Create Procedure [dbo].[TA_GetDataTOIndex]
	@RETURN_VALUE VARCHAR(100) OUTPUT
	,@INDEX INT
	,@DATA1 VARCHAR(100)
	,@DATA2 VARCHAR(100)
	,@DATA3 VARCHAR(100)
	,@DATA4 VARCHAR(100)
	,@DATA5 VARCHAR(100)
	,@DATA6 VARCHAR(100)
	,@DATA7 VARCHAR(100)
	,@DATA8 VARCHAR(100)
	,@DATA9 VARCHAR(100)
	,@DATA10 VARCHAR(100)
	,@DATA11 VARCHAR(100)
	,@DATA12 VARCHAR(100)
	,@DATA13 VARCHAR(100)
	,@DATA14 VARCHAR(100)
	,@DATA15 VARCHAR(100)
As
	Select @RETURN_VALUE= Case @INDEX WHEN 1 THEN @DATA1
		WHEN 2 THEN @DATA2
		WHEN 3 THEN @DATA3
		WHEN 4 THEN @DATA4
		WHEN 5 THEN @DATA5
		WHEN 6 THEN @DATA6
		WHEN 7 THEN @DATA7
		WHEN 8 THEN @DATA8
		WHEN 9 THEN @DATA9
		WHEN 10 THEN @DATA10
		WHEN 11 THEN @DATA11
		WHEN 12 THEN @DATA12
		WHEN 13 THEN @DATA13
		WHEN 14 THEN @DATA14
		WHEN 15 THEN @DATA15
		ELSE ''
		END

	RETURN (0)

GO
